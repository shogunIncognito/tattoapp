<script>
export default {
  data() {
    return {
      formData: {
        name: "",
        email: "",
        message: "",
      },
      loading: false,
      successMessage: "",
      errorMessage: "",
    };
  },
  methods: {
    async submitForm() {
      this.loading = true;
      this.successMessage = "";
      this.errorMessage = "";

      try {
        const response = await fetch("https://formsubmit.co/ajax/d636d91bf5a448a0bb934bfad607e367", {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            Accept: "application/json",
          },
          body: JSON.stringify(this.formData),
        });

        const result = await response.json();

        if (result.success) {
          this.successMessage = "¡Gracias! Tu reporte ha sido enviado.";
          this.formData = { name: "", email: "", message: "" }; // Limpiar el formulario
        } else {
          this.errorMessage = "Hubo un error al enviar el reporte. Inténtalo de nuevo.";
        }
      } catch (error) {
        this.errorMessage = "Error de conexión. Por favor, inténtalo más tarde.";
        console.error("Error al enviar el formulario:", error);
      } finally {
        this.loading = false;
      }
    },
  },
};
</script>


